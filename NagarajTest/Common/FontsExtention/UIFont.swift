

import UIKit

extension UIFont {
    class var normalTextFont: UIFont {
        return UIFont.systemFont(ofSize: 17.0, weight: .regular)
    }

    class var listBoldTextFont: UIFont {
        return UIFont.systemFont(ofSize: 12.0, weight: .bold)
    }

    class var smallTextFont: UIFont {
        return UIFont.systemFont(ofSize: 12.0, weight: .regular)
    }
}
